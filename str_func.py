def uppercase_string(input_string):
    """
    Принимает строку и возвращает её в верхнем регистре.

    Args:
        input_string (str): Входная строка.

    Returns:
        str: Строка в верхнем регистре.
    """
    return input_string.upper()

def capitalize_words(input_string):
    """
    Принимает строку и делает заглавными первые буквы каждого слова.

    Args:
        input_string (str): Входная строка.

    Returns:
        str: Строка с заглавными первыми буквами каждого слова.
    """
    return input_string.title()
